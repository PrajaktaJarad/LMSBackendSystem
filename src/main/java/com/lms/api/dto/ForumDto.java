package com.lms.api.dto;

public class ForumDto {
	
	private int totalNoOfTopics;
	private int totalNoOfQue;
	private int totalNoOfAns;
	private int totalNoOfUsers;
	
	public int getTotalNoOfTopics() {
		return totalNoOfTopics;
	}
	public void setTotalNoOfTopics(int totalNoOfTopics) {
		this.totalNoOfTopics = totalNoOfTopics;
	}
	public int getTotalNoOfQue() {
		return totalNoOfQue;
	}
	public void setTotalNoOfQue(int totalNoOfQue) {
		this.totalNoOfQue = totalNoOfQue;
	}
	public int getTotalNoOfAns() {
		return totalNoOfAns;
	}
	public void setTotalNoOfAns(int totalNoOfAns) {
		this.totalNoOfAns = totalNoOfAns;
	}
	public int getTotalNoOfUsers() {
		return totalNoOfUsers;
	}
	public void setTotalNoOfUsers(int totalNoOfUsers) {
		this.totalNoOfUsers = totalNoOfUsers;
	}
	

}
